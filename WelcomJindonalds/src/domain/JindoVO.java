package domain;

public class JindoVO 
{
	int no;
	String id, pw, name, money;
	
	public int getNo() 
	{
		return no;
	}
	
	public void setNo(int no) 
	{
		this.no = no;
	}
	
	public String getMoney() 
	{
		return money;
	}
	
	public void setMoney(String money) 
	{
		this.money = money;
	}
	
	public String getId() 
	{
		return id;
	}
	
	public void setId(String id) 
	{
		this.id = id;
	}
	
	public String getPw() 
	{
		return pw;
	}
	
	public void setPw(String pw) 
	{
		this.pw = pw;
	}

	public String getName() 
	{
		return name;
	}
	
	public void setName(String name) 
	{
		this.name = name;
	}
}
