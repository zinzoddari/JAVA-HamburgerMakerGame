package domain;

public class HambergerVO 
{
	int hamNum;
	String hamName;
	String hamPrice;
	
	public int getHamNum() 
	{
		return hamNum;
	}
	public void setHamNum(int hamNum) 
	{
		this.hamNum = hamNum;
	}
	public String getHamName() 
	{
		return hamName;
	}
	public void setHamName(String hamName) 
	{
		this.hamName = hamName;
	}
	public String getHamPrice() 
	{
		return hamPrice;
	}
	public void setHamPrice(String hamPrice) 
	{
		this.hamPrice = hamPrice;
	}
}
